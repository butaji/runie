//! GrokDay theme builder.

use opaline::{OpalineColor, Theme, ThemeBuilder, ThemeVariant};
use crate::theme::themes::apply_extra_tokens;

pub fn build_grok_day() -> Theme {
    let mut b = Theme::builder("grok-day")
        .variant(ThemeVariant::Light)
        .author("Runie")
        .description("GrokDay - bright light theme");
    b = add_grok_day_palettes(b);
    b = add_grok_day_tokens(b);
    apply_extra_tokens(b).build()
}

fn add_grok_day_palettes(b: ThemeBuilder) -> ThemeBuilder {
    b.palette("base", OpalineColor::new(0xF8, 0xF9, 0xFA))
        .palette("light", OpalineColor::new(0xFF, 0xFF, 0xFF))
        .palette("dark", OpalineColor::new(0xE9, 0xEC, 0xEF))
        .palette("user_accent", OpalineColor::new(0x05, 0x8A, 0x4F))
        .palette("thinking_accent", OpalineColor::new(0xD4, 0xA0, 0x4E))
        .palette("tool_accent", OpalineColor::new(0x21, 0x68, 0xC0))
        .palette("error_accent", OpalineColor::new(0xDC, 0x26, 0x26))
        .palette("success_accent", OpalineColor::new(0x10, 0xB9, 0x81))
}

