//! RosePineMoon theme builder.

use opaline::{OpalineColor, Theme, ThemeBuilder, ThemeVariant};
use crate::theme::themes::apply_extra_tokens;

pub fn build_rose_pine_moon() -> Theme {
    let mut b = Theme::builder("rose-pine-moon")
        .variant(ThemeVariant::Dark)
        .author("Runie")
        .description("RosePineMoon - deep purple-gray theme");
    b = add_rose_pine_moon_palettes(b);
    b = add_rose_pine_moon_tokens(b);
    apply_extra_tokens(b).build()
}

fn add_rose_pine_moon_palettes(b: ThemeBuilder) -> ThemeBuilder {
    b.palette("base", OpalineColor::new(0x23, 0x21, 0x36))
        .palette("light", OpalineColor::new(0x2A, 0x27, 0x40))
        .palette("dark", OpalineColor::new(0x1F, 0x1A, 0x2E))
        .palette("user_accent", OpalineColor::new(0xC4, 0xA7, 0xE7))
        .palette("thinking_accent", OpalineColor::new(0xF6, 0xC1, 0x77))
        .palette("tool_accent", OpalineColor::new(0xEB, 0xBC, 0xBA))
        .palette("error_accent", OpalineColor::new(0xEB, 0x6F, 0x92))
        .palette("success_accent", OpalineColor::new(0x9C, 0xCF, 0xD0))
}

