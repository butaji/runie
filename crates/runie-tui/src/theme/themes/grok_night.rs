//! GrokNight theme builder.

use opaline::{OpalineColor, Theme, ThemeBuilder, ThemeVariant};
use crate::theme::themes::apply_extra_tokens;

pub fn build_grok_night() -> Theme {
    let mut b = Theme::builder("grok-night")
        .variant(ThemeVariant::Dark)
        .author("Runie")
        .description("GrokNight - deep navy default dark theme");
    b = add_grok_night_palettes(b);
    b = add_grok_night_tokens(b);
    apply_extra_tokens(b).build()
}

fn add_grok_night_palettes(b: ThemeBuilder) -> ThemeBuilder {
    b.palette("base", OpalineColor::new(0x1A, 0x1A, 0x2E))
        .palette("light", OpalineColor::new(0x16, 0x21, 0x3E))
        .palette("dark", OpalineColor::new(0x0F, 0x0F, 0x1A))
        .palette("user_accent", OpalineColor::new(0x6E, 0xE7, 0xB7))
        .palette("thinking_accent", OpalineColor::new(0xFB, 0xBF, 0x24))
        .palette("tool_accent", OpalineColor::new(0x60, 0xA5, 0xFA))
        .palette("error_accent", OpalineColor::new(0xF8, 0x71, 0x71))
        .palette("success_accent", OpalineColor::new(0x34, 0xD3, 0x99))
}

