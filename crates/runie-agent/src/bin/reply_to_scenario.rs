//! `reply_to_scenario` — drive the ReplyProvider with a user message,
//! capture the AgentEvent stream, write it as a JSONL scenario file.
//!
//! This is the bridge from "reply provider data simulating real API
//! behaviour" → "replayable scenario file" → "renderable TUI state".
//!
//! # Usage
//!
//! ```bash
//! # Capture a Simple scenario (default for most prompts)
//! cargo run -p runie-ai --bin reply_to_scenario --release -- \
//!     "hello" ui/dumps/scenarios/01_simple.jsonl
//!
//! # Force a specific scenario
//! cargo run -p runie-ai --bin reply_to_scenario --release -- \
//!     "calculate 2+2" ui/dumps/scenarios/02_tool.jsonl --scenario tool
//!
//! # Then render it and diff against Grok
//! target/release/scenario_replay ui/dumps/scenarios/01_simple.jsonl \
//!     --diff ui/dumps/grok/03_chat.txt
//! ```
//!
//! The user message is always written as the first AgentEvent so the
//! scenario drives the TUI from the empty state through to completion.

use std::path::PathBuf;
use std::process::ExitCode;

use runie_ai::providers::ReplyProvider;
use runie_ai::providers::reply::Scenario;
use runie_agent::events::AgentEvent;
use runie_core::Event as ProviderEvent;

fn main() -> ExitCode {
    let args: Vec<String> = std::env::args().collect();
    if args.len() < 3 {
        eprintln!("usage: reply_to_scenario <user-message> <output.jsonl> [--scenario simple|tool|stream|streamtool|error|context|longreasoning]");
        return ExitCode::from(2);
    }
    let user_msg = &args[1];
    let out_path = PathBuf::from(&args[2]);

    // Optional --scenario flag (otherwise inferred from message text)
    let mut forced: Option<Scenario> = None;
    let mut i = 3;
    while i < args.len() {
        match args[i].as_str() {
            "--scenario" => {
                i += 1;
                let s = &args[i];
                forced = Some(match s.as_str() {
                    "simple" => Scenario::Simple,
                    "tool" => Scenario::Tool,
                    "stream" => Scenario::Stream,
                    "streamtool" => Scenario::StreamTool,
                    "error" => Scenario::Error,
                    "context" => Scenario::Context,
                    "longreasoning" => Scenario::LongReasoning,
                    other => {
                        eprintln!("unknown scenario '{other}'");
                        return ExitCode::from(2);
                    }
                });
            }
            _ => {
                eprintln!("unexpected arg: {}", args[i]);
                return ExitCode::from(2);
            }
        }
        i += 1;
    }

    // Build the ReplyProvider
    let provider = match ReplyProvider::with_default_fixtures() {
        Ok(p) => p,
        Err(e) => {
            eprintln!("error loading fixtures: {e}");
            return ExitCode::from(1);
        }
    };

    // Decide which scenario to use
    let scenario = forced.unwrap_or_else(|| Scenario::from_input(user_msg));
    eprintln!("[reply_to_scenario] scenario: {scenario:?}");
    eprintln!("[reply_to_scenario] user message: {user_msg:?}");

    // Generate the events (sync, no actual network)
    let events = provider.generate_events_for_scenario(scenario);

    // Convert to AgentEvent JSONL. We prepend the user message event so
    // the scenario drives the TUI from the empty state through to completion.
    let mut out = String::new();
    out.push_str(&format!(
        "# Generated by reply_to_scenario\n# user: {user_msg}\n# scenario: {scenario:?}\n# events: {}\n",
        events.len() + 1
    ));
    // Default UI state: chat mode, home hidden, realistic token values.
    // The scenario can override these by sending ui ops.
    out.push_str(
        r#"{"__meta__": true, "width": 80, "height": 24}
{"ui": true, "kind": "home_visible", "value": false}
{"ui": true, "kind": "mode", "value": "chat"}
"#,
    );
    // User message first
    let user_event = AgentEvent::Message {
        role: "user".to_string(),
        content: user_msg.clone(),
    };
    out.push_str(&serde_json::to_string(&user_event).unwrap());
    out.push('\n');

    // Pre-create a placeholder assistant message so thinking_end can
    // attach thought_duration to it (Grok renders "◆ Thought for 1.2s"
    // attached to the assistant that follows).
    if let AgentEvent::MessageUpdate { .. } = &events.first().cloned().unwrap_or(AgentEvent::Message {
        role: String::new(),
        content: String::new(),
    }) {
        // No-op: only the MessageStart matters below
    }
    // We need an assistant MessageItem to exist before thinking_end.
    // The TUI's on_thinking_end sets thought_duration on state.messages.last().
    // There's no public way to do that from an AgentEvent, so we skip
    // this for now and rely on the assistant message itself being
    // rendered first (the scenario ReplayProvider emits MessageStart for
    // assistant before ThinkingStart in some flows).

    // Then provider events. The provider emits its own Event enum (not
    // AgentEvent) so we map them. We also track usage so we can inject a
    // session_tokens UI op after AgentEnd.
    let mut final_usage: Option<(u32, u32, u32)> = None;
    for ev in &events {
        if let Some(agent_ev) = provider_event_to_agent_event(ev) {
            // Capture usage from the AgentEnd so we can inject the op
            if let AgentEvent::AgentEnd { final_token_usage, .. } = &agent_ev {
                final_usage = Some((
                    final_token_usage.input,
                    final_token_usage.output,
                    final_token_usage.total_tokens,
                ));
            }
            out.push_str(&serde_json::to_string(&agent_ev).unwrap());
            out.push('\n');
        }
    }
    if let Some((_, _, total)) = final_usage {
        out.push_str(&format!(
            r#"{{"ui": true, "kind": "session_tokens", "value": {{"total": {total}}}}}"#,
        ));
        out.push('\n');
    }

    if let Err(e) = std::fs::write(&out_path, &out) {
        eprintln!("error writing {}: {e}", out_path.display());
        return ExitCode::from(1);
    }
    eprintln!(
        "[reply_to_scenario] wrote {} ({} bytes, {} events)",
        out_path.display(),
        out.len(),
        events.len() + 1
    );
    ExitCode::from(0)
}

/// Map a provider-level `Event` to a runie-agent `AgentEvent`. The mapping
/// is lossy (some fields are filled in with placeholders) but sufficient
/// to drive the TUI.
fn provider_event_to_agent_event(ev: &ProviderEvent) -> Option<AgentEvent> {
    use ProviderEvent as P;
    let empty_msg = || runie_agent::events::AgentMessage {
        role: "assistant".to_string(),
        content: Vec::new(),
        timestamp: 0,
        usage: None,
        stop_reason: None,
        error_message: None,
        tool_calls: Vec::new(),
    };
    match ev {
        P::MessageStart { role, .. } => {
            if role == "assistant" {
                Some(AgentEvent::MessageStart { message: empty_msg(), turn: 0 })
            } else {
                None
            }
        }
        P::MessageDelta { content } if !content.is_empty() => Some(AgentEvent::MessageUpdate {
            message: empty_msg(),
            delta: content.clone(),
            replace: false,
            turn: 0,
        }),
        P::MessageEnd => Some(AgentEvent::MessageEnd { message: empty_msg(), turn: 0 }),
        P::ThinkingDelta { content } if !content.is_empty() => Some(AgentEvent::ThinkingUpdate {
            delta: content.clone(),
            total_len: content.len(),
            turn: 0,
        }),
        P::ToolCallDelta { id, name, arguments } => Some(AgentEvent::ToolExecutionStart {
            tool_call_id: id.clone(),
            tool_name: name.clone(),
            tool_args: arguments.clone(),
            turn: 0,
        }),
        P::ToolExecutionStart { tool_call_id, tool_name, args, .. } => {
            Some(AgentEvent::ToolExecutionStart {
                tool_call_id: tool_call_id.clone(),
                tool_name: tool_name.clone(),
                tool_args: args.to_string(),
                turn: 0,
            })
        }
        P::Usage { prompt_tokens, completion_tokens, total_tokens } => {
            // Add a scenario UI op so the top bar gauge shows the right
            // numbers, then end the agent.
            Some(AgentEvent::AgentEnd {
                messages: Vec::new(),
                total_turns: 1,
                final_token_usage: runie_agent::events::TokenUsage {
                    input: *prompt_tokens as u32,
                    output: *completion_tokens as u32,
                    cache_read: 0,
                    cache_write: 0,
                    total_tokens: *total_tokens as u32,
                },
            })
        }
        P::Error { message } => Some(AgentEvent::Error {
            message: message.clone(),
            error_type: "provider".to_string(),
            recoverable: false,
            context: String::new(),
        }),
        _ => None,
    }
}

